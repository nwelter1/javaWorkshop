public class DataTypes {
    public static void main(String[] args) {
        // String and char
        String school = "Coding Temple";
        System.out.println("My coding school of choice is " + school);
        char grade = 'A';
        System.out.println("I got an " + grade + " this semester!");
        // numerical data types
        long age = 30L;
        System.out.println("Pretty impressive for a " + age + " year old!");
        long googleSearches = 5800000000L;
        System.out.println("Google Searches: " + googleSearches);
        double percentage = 99.7;
        System.out.println("That's better than " + percentage + " percent of students in my class!");
    }
}